package com.example.reportcardclass;

public class ReportCard {
    private String name, englishGrade, histroyGrade, mathGrade ;

    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return this.name;
    }

    public void setEnglishGrade(String englishGrade){
        this.name = englishGrade;
    }
    public String getEnglishGrade(){
        return this.englishGrade;
    }

    public void setHistroyGrade(String histroyGrade){
        this.name = histroyGrade;
    }
    public String getHistroyGrade(){
        return this.histroyGrade;
    }

    public void setMathGrade(String mathGrade){
        this.name = mathGrade;
    }
    public String getMathGrade(){
        return this.mathGrade;
    }







}
